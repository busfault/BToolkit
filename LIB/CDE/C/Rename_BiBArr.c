void
Rename_BiBA_STO(int _idx,int _vv)
{
  int nx;
  Rename_B_STO(_idx,_vv);
  BINC(&nx,_idx);
  if ( nx < Rename_BiBArrP2 ) {
    Rename_N_INC();
  }
  else {
    Rename_N_STO(Rename_BiBArrP1);
  }
}


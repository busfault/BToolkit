void
INI_Rename_BiBArr(void)
{
  int idx = Rename_BiBArrP1;
  INI_Rename_B_BiArr();
  INI_Rename_N_Byt();
  Rename_N_STO(Rename_BiBArrP2);
  while (idx <= Rename_BiBArrP2) {
    Rename_B_STO(idx++,Rename_BiBArrP3);
  }
}

